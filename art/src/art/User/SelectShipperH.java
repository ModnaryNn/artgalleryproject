    private void shipperTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_shipperTableMouseClicked
        int index = shipperTable.getSelectedRow();
        TableModel model = shipperTable.getModel();
        
        String id = model.getValueAt(index,0).toString();
        selectedID = Integer.parseInt(id);
        idTXT.setText(id);
        
        String shipperName = model.getValueAt(index,1).toString();
        shippernameTXT.setText(shipperName);
        
        String phone = model.getValueAt(index,2).toString();
        phoneTXT.setText(phone);
        
        proceedBUTTON.setEnabled(true);  
    }//GEN-LAST:event_shipperTableMouseClicked

    private void proceedBUTTONActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proceedBUTTONActionPerformed
        try{
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("INSERT INTO orders (user_id, shipper_id, artwork_id, order_time) VALUE (?,?,?,now())");
            ps.setInt(1,userID);
            ps.setInt(2,selectedID);
            ps.setInt(3,artworkID);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null,"Order Sucessfully");
            setVisible(false);
            dispose();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }  
    }//GEN-LAST:event_proceedBUTTONActionPerformed

