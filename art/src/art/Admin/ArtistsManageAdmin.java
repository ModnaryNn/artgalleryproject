    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String name = nameTXT.getText();
        String about = aboutTXT.getText();    
        String birth = birthTXT.getText(); 
        String death = deathTXT.getText(); 

        if (validateFields("new")){
            JOptionPane.showMessageDialog(null,"All fields are required");
        }
        else {
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("INSERT INTO artists(fullname, about, birth_year, death_year) VALUES (?,?,?,?)");
                ps.setString(1,name);
                ps.setString(2,about);
                ps.setString(3,birth);
                ps.setString(4,death);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Added Sucessfully");
                setVisible(false);
                dispose(); 
                new ArtistsManageAdmin().setVisible(true);
            }
            catch (Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        String name = nameTXT.getText();
        String about = aboutTXT.getText();    
        String birth = birthTXT.getText(); 
        String death = deathTXT.getText(); 
        
        if (validateFields("edit")){
            JOptionPane.showMessageDialog(null,"All fields are required");
        }
        else {
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("UPDATE artists SET fullname = ?,about= ?,birth_year = ?, death_year = ? WHERE id = ?");
                ps.setString(1,name);
                ps.setString(2,about);
                ps.setString(3,birth);
                ps.setString(4,death);
                ps.setInt(5,selectedID);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Updated Sucessfully");
                setVisible(false);
                dispose(); 
                new ArtistsManageAdmin().setVisible(true);
            }
            catch (Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_updateButtonActionPerformed

