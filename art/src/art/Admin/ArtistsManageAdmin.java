    private boolean validateFields(String formType){
        if(formType.equals("edit") && !idTXT.getText().equals("") && !nameTXT.getText().equals("") && !birthTXT.getText().equals("") && !deathTXT.getText().equals("") ){
            return false;
        }
        else if (formType.equals("new") && (!nameTXT.getText().equals("") &&  !birthTXT.getText().equals("") && !deathTXT.getText().equals(""))){
            return false;
        }
        else{
            return true;
        }
    }
    

