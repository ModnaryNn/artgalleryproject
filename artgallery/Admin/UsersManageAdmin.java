    private boolean validateFields(String formType){
        if(formType.equals("edit") && !idTXT.getText().equals("") && !firstNameTXT.getText().equals("") && !lastNameTXT.getText().equals("") && !phoneTXT.getText().equals("") && !addressTXT.getText().equals("") && !accountidTXT.getText().equals("") ){
            return false;
        }
        else if (formType.equals("new") && (!idTXT.getText().equals("") && !firstNameTXT.getText().equals("") && !lastNameTXT.getText().equals("") && !phoneTXT.getText().equals("") && !addressTXT.getText().equals("") && !accountidTXT.getText().equals(""))){
            return false;
        }
        else{
            return true;
        }
    }
    

