    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String firstName = firstNameTXT.getText();
        String lastName = lastNameTXT.getText();
        String phone = phoneTXT.getText();
        String address = addressTXT.getText();
        int accountID = Integer.parseInt(accountidTXT.getText());
        
        if (validateFields("new")){
            JOptionPane.showMessageDialog(null,"All fields are required");
        }
        else {
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("INSERT INTO users(first_name, last_name, phone, address, account_id) VALUES (?,?,?,?,?)");
                ps.setString(1,firstName);
                ps.setString(2,lastName);
                ps.setString(3,phone);
                ps.setString(4,address);
                ps.setInt(5,accountID);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Added Sucessfully");
                setVisible(false);
                dispose(); 
                new UsersManageAdmin().setVisible(true);
            }
            catch (Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void userTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userTableMouseClicked
        int index = userTable.getSelectedRow();
        TableModel model = userTable.getModel();
        
        String id = model.getValueAt(index,0).toString();
        selectedID = Integer.parseInt(id);
        idTXT.setText(id);
        
        String firstname = model.getValueAt(index,1).toString();
        firstNameTXT.setText(firstname);
        
        String lastname = model.getValueAt(index,2).toString();
        lastNameTXT.setText(lastname);
        
        String phone = model.getValueAt(index,3).toString();
        phoneTXT.setText(phone);
        
        String address = model.getValueAt(index,4).toString();
        addressTXT.setText(address);
        
        String accountid = model.getValueAt(index,5).toString();
        accountidTXT.setText(accountid);
        
        saveButton.setEnabled(false);
        updateButton.setEnabled(true);  
        deleteButton.setEnabled(true);  
    }//GEN-LAST:event_userTableMouseClicked

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        String firstName = firstNameTXT.getText();
        String lastName = lastNameTXT.getText();
        String phone = phoneTXT.getText();
        String address = addressTXT.getText();
        int accountID = Integer.parseInt(accountidTXT.getText());
        
        if (validateFields("edit")){
            JOptionPane.showMessageDialog(null,"All fields are required");
        }
        else {
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("UPDATE users SET first_name = ?,last_name= ?,phone = ?, address = ?, account_id = ? WHERE id = ?");
                ps.setString(1,firstName);
                ps.setString(2,lastName);
                ps.setString(3,phone);
                ps.setString(4,address);
                ps.setInt(5,accountID);
                ps.setInt(6,selectedID);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Updated Sucessfully");
                setVisible(false);
                dispose(); 
                new UsersManageAdmin().setVisible(true);
            }
            catch (Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_updateButtonActionPerformed

