    private boolean validateFields(String formType){
        if(formType.equals("edit") && !idTXT.getText().equals("") && !titleTXT.getText().equals("") && !aboutTXT.getText().equals("") && !createdyearTXT.getText().equals("") && !galleryidTXT.getText().equals("") && !artistidTXT.getText().equals("")){
            return false;
        }
        else if (formType.equals("new") && (!titleTXT.getText().equals("") && !aboutTXT.getText().equals("") && !createdyearTXT.getText().equals("") && !galleryidTXT.getText().equals("") && !artistidTXT.getText().equals(""))){
            return false;
        }
        else{
            return true;
        }
    }
    

