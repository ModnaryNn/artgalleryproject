    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String title = titleTXT.getText();
        String about = aboutTXT.getText();
        int createdyear = Integer.parseInt(createdyearTXT.getText());
        String isforsale = (String) forsaleCB.getSelectedItem();
        boolean check;
        if("True".equals(isforsale)){
            check = true;
        }
        else {
            check = false;
        }
        double price = Double.parseDouble(priceTXT.getText());
        int galleryid = Integer.parseInt(galleryidTXT.getText());
        int artistid = Integer.parseInt(artistidTXT.getText());
        
        if (validateFields("new")){
            JOptionPane.showMessageDialog(null,"All fields are required");
        }
        else {
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("INSERT INTO artworks(title, about, created_year,is_for_sale,price, gallery_id, artist_id) VALUES (?,?,?,?,?,?,?)");
                ps.setString(1,title);
                ps.setString(2, about);
                ps.setInt(3, createdyear);
                ps.setBoolean(4,check);
                ps.setDouble(5, price);
                ps.setInt(6, galleryid);
                ps.setInt(7, artistid);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Added Sucessfully");
                setVisible(false);
                dispose(); 
                new ArtworksManageAdmin().setVisible(true);
            }
            catch (Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void artworkTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_artworkTableMouseClicked
        int index = artworkTable.getSelectedRow();
        TableModel model = artworkTable.getModel();
        
        String id = model.getValueAt(index,0).toString();
        selectedID = Integer.parseInt(id);
        idTXT.setText(id);
        
        String title = model.getValueAt(index,1).toString();
        titleTXT.setText(title);
        
        String about = model.getValueAt(index,2).toString();
        aboutTXT.setText(about);
        
        String createdyear = model.getValueAt(index, 3).toString();
        createdyearTXT.setText(createdyear);
        
        String forsale = model.getValueAt(index,4).toString();
        if("0".equals(forsale)){
           forsaleCB.removeAllItems();
           forsaleCB.addItem("False");        
           forsaleCB.addItem("True");

        }
        else {
           forsaleCB.removeAllItems();
           forsaleCB.addItem("True");
           forsaleCB.addItem("False");
        }
        
        String price ="0";
        price = model.getValueAt(index,5).toString();
        priceTXT.setText(price);
        
        String galleryid = model.getValueAt(index,6).toString();
        galleryidTXT.setText(galleryid);

        String artistid = model.getValueAt(index,7).toString();
        artistidTXT.setText(artistid);     
        
        saveButton.setEnabled(false);
        updateButton.setEnabled(true);  
        deleteButton.setEnabled(true);
    }//GEN-LAST:event_artworkTableMouseClicked

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed

        String title = titleTXT.getText();
        String about = aboutTXT.getText();
        int createdyear = Integer.parseInt(createdyearTXT.getText());
        String isforsale = (String) forsaleCB.getSelectedItem();
        boolean check;
        if("True".equals(isforsale)){
            check = true;
        }
        else {
            check = false;
        }
        double price = Double.parseDouble(priceTXT.getText());
        int galleryid = Integer.parseInt(galleryidTXT.getText());
        int artistid = Integer.parseInt(artistidTXT.getText());
        
        if (validateFields("edit")){
            JOptionPane.showMessageDialog(null,"All fields are required");
        }
        else {
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("UPDATE artworks SET title=?, about=?, created_year = ?, is_for_sale = ?, price = ?, gallery_id = ?, artist_id = ? WHERE id = ?");
                ps.setString(1,title);
                ps.setString(2, about);
                ps.setInt(3, createdyear);
                ps.setBoolean(4,check);
                ps.setDouble(5, price);
                ps.setInt(6, galleryid);
                ps.setInt(7, artistid);
                ps.setInt(8,selectedID);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Updated Sucessfully");
                setVisible(false);
                dispose(); 
                new ArtworksManageAdmin().setVisible(true);
            }
            catch (Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_updateButtonActionPerformed

